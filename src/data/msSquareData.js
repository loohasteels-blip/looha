const L = '6m';
const U = 'Nos';

function sq(size, thickness, wpm) {
    const sid = size.replace(/ x /g, 'x');
    const tid = String(thickness).replace('.', '_');
    return {
        id: `mssq-${sid}-${tid}`,
        size,
        thickness,
        weightPerMeter: wpm,
        weightPerPiece: parseFloat((wpm * 6).toFixed(3)),
        pricePerTon: P,
        unit: U,
        length: L,
    };
}

